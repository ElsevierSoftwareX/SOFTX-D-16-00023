      NEF=19           !Table 4 Fundamental elements of the ecliptic                                                 
      DO  I=1,NEF                                                      
        BE(I)=BE(I)*PIRR                                                      
        CE(I)=CE(I)*PIR                                                       
                                                     
      END DO         ! calculate e.sin.pi                                                
!
!   ------------------------------
!   |                            |
!   |       Part 2 (table 1)     |
!   ------------------------------

!                                                                       
!   OBLIQUITY                                                           
!                                                                       
      XOD=23.320556D0   ! Berger's 1978 paper 23o320 556                                                  
      NOB=47            ! retrieve the whole 47 terms in Table 1 of Berger78 JAS paper                                               
