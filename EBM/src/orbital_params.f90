      DO  I=1,NEF                                                     
        ARG=BE(I)*T+CE(I)                                                 
        XES=XES+AE(I)*DSIN(ARG)   !e.sin.pi                                        
        XEC=XEC+AE(I)*DCOS(ARG)   !e.cos.pi                                        
      END DO
                                                          
      ECC=DSQRT(XES*XES+XEC*XEC)  !eccentricity calculated                                     
      TRA=DABS(XEC) !   
                                                 
      IF(TRA.LE.1.0D-08) GO TO 10                                       
      RP=DATAN(XES/XEC)                                               
      IF(XEC) 11,10,12                                                  
11    RP=RP+PI                                                          
      GO TO 13                                                          
12    IF(XES) 14,13,13                                                  
14    RP=RP+2.0D0*PI                                                    
      GO TO 13                                                          
10    IF(XES) 15,16,17                                                  
15    RP=1.5D0*PI                                                       
      GO TO 13                                                          
16    RP=0.0D0                                                          
      GO TO 13                                                          
17    RP=PI/2.0D0                                                       
13    PERH=RP/PIR   
                                                               
      PRG=PRM*T    
                                                     
      DO I=1,NOP                                                      
        ARG=BOP(I)*T+COP(I)                                               
        PRG=PRG+AOP(I)*DSIN(ARG)                                          
      END DO
                                                         
      PRG=PRG/3600.0D0+XOP                                              
      PERH=PERH+PRG                                                     
54    IF(PERH) 51,55,53                                                 
51    PERH=PERH+360.0D0                                                 
      GO TO 54                                                          
53    IF(PERH.LT.360.0D0) GO TO 55                                      
      PERH=PERH-360.0D0                                                 
      GO TO 53                                                          
55    CONTINUE       !longitude of the perihelion calculated                                                 
!                                                                       
      PRE=ECC*DSIN(PERH*PIR)                                            
!                                                                       
      XOB=XOD                                                           
      DO  I=1,NOB                                                     
        ARG=BOB(I)*T+COB(I)                                               
        XOB=XOB+AOB(I)/3600.0D0*DCOS(ARG)                                 
      END DO   ! obliquity calculated        
      ECCR=ECC
      XOBR= XOB*PI/180.0 
      PERHR= PERH*PI/180.0 

      RETURN                                                              
END SUBROUTINE ORBITAL_PARAMS 

